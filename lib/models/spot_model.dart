class Spot {
  final String id;
  final String title;
  final String description;
  final String category;
  final double distance;
  final String userId;
  final String userName;

  Spot({
    required this.id,
    required this.title,
    required this.description,
    required this.category,
    required this.distance,
    required this.userId,
    required this.userName,
  });
}
